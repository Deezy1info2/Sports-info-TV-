tasks.register("copyApkOutputs") {
    doLast {
        val srcFile = file("${buildDir}/outputs/apk/debug/app-debug.apk")
        if (srcFile.exists()) {
            val dest1 = file("${rootDir}/.build-outputs/app-debug.apk")
            val dest2 = file("${rootDir}/APK_DOWNLOAD/app-debug.apk")
            
            dest1.parentFile.mkdirs()
            dest2.parentFile.mkdirs()
            
            srcFile.copyTo(dest1, overwrite = true)
            srcFile.copyTo(dest2, overwrite = true)
            
            val sizeBytes = dest2.length()
            val sizeMB = sizeBytes.toDouble() / (1024 * 1024)
            println("Successfully copied APK to ${dest1.absolutePath} and ${dest2.absolutePath}")
            println("Copied APK file size: $sizeBytes bytes (${String.format("%.2f", sizeMB)} MB)")
            
            if (sizeBytes == 0L) {
                throw GradleException("Generated APK is 0 bytes! It must be a valid build.")
            }
        } else {
            throw GradleException("Source APK not found at ${srcFile.absolutePath}")
        }
    }
}

